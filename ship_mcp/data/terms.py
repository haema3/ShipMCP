# Index by ID for fast lookup
TERMS_BY_ID: dict[str, Term] = {t["id"]: t for t in ALL_TERMS}

# Index by English term (lowercase)
TERMS_BY_ENGLISH: dict[str, Term] = {t["term_en"].lower(): t for t in ALL_TERMS}

# Index by Korean term
TERMS_BY_KOREAN: dict[str, Term] = {}
for t in ALL_TERMS:
    key = t["term_ko"].split("(")[0].strip().lower()
    TERMS_BY_KOREAN[key] = t
    # Also index full term including parentheses
    TERMS_BY_KOREAN[t["term_ko"].lower()] = t


def get_term(term_id: str) -> Term | None:
    """Look up a term by its unique ID."""
    return TERMS_BY_ID.get(term_id)


def get_term_by_name(name: str) -> Term | None:
    """Look up a term by English or Korean name (case-insensitive)."""
    name_lower = name.lower().strip()
    # Try English first
    if name_lower in TERMS_BY_ENGLISH:
        return TERMS_BY_ENGLISH[name_lower]
    # Try Korean
    if name_lower in TERMS_BY_KOREAN:
        return TERMS_BY_KOREAN[name_lower]
    return None


def search_terms(query: str, max_results: int = 20) -> list[Term]:
    """Search terms by keyword in English term, Korean term, or description."""
    q = query.lower().strip()
    results: list[Term] = []

    for term in ALL_TERMS:
        if q in term["term_en"].lower():
            results.append(term)
            continue
        if q in term["term_ko"].lower():
            results.append(term)
            continue
        if q in term["description_en"].lower():
            results.append(term)
            continue
        if q in term["description_ko"].lower():
            results.append(term)
            continue
        if term["abbreviation"] and q in term["abbreviation"].lower():
            results.append(term)
            continue

    return results[:max_results]


def get_terms_by_category(category_id: str) -> list[Term]:
    """Get all terms belonging to a category."""
    return [t for t in ALL_TERMS if t["category"] == category_id]


def get_related_terms(term: Term) -> list[Term]:
    """Get the related terms for a given term."""
    related = []
    for rel_id in term.get("related_terms", []):
        rel = get_term(rel_id)
        if rel:
            related.append(rel)
    return related
